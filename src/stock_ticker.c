static char *find_char(const char *s, char c)
{
    while (*s) { if (*s == c) return (char*)s; s++; }
    return NULL;
}

static const char *find_substr(const char *hay, const char *needle)
{
    if (!*needle) return hay;
    while (*hay) {
        const char *h = hay;
        const char *n = needle;
        while (*h && *n && (*h == *n)) { h++; n++; }
        if (*n == '\0') return hay;
        hay++;
    }
    return NULL;
}

