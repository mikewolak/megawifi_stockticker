/* Convert 3-letter month abbreviation to month number 1-12 (0 = unknown). */
static u8 month_from_abbr(const char *m)
{
    static const char mo[12][4] = {
        "Jan","Feb","Mar","Apr","May","Jun",
        "Jul","Aug","Sep","Oct","Nov","Dec"
    };
    u8 i;
    for (i = 0; i < 12; i++)
        if (m[0] == mo[i][0] && m[1] == mo[i][1] && m[2] == mo[i][2])
            return (u8)(i + 1);
    return 0;
}

/* -------------------------------------------------------------------------
 * status_clear() / status_err()
 * STATUS_ROW is blank during normal operation; red text on error.
 * ---------------------------------------------------------------------- */
static void status_clear(void)
{
    VDP_setTextPalette(PAL0);
    VDP_drawText("                                        ", 0, STATUS_ROW);
}

static void status_err(const char *msg)
{
    char buf[42];
    uint8_t i = 0;
    while (msg[i] && i < 39) { buf[i] = msg[i]; i++; }
    while (i < 40) buf[i++] = ' ';
    buf[40] = '\0';
    VDP_setTextPalette(PAL2); /* red */
    VDP_drawText(buf, 0, STATUS_ROW);
    VDP_setTextPalette(PAL0);
    VDP_waitVSync();
}

/* Used only during init for one-time progress messages */
static void status_info(const char *msg)
{
    char buf[42];
    uint8_t i = 0;
    while (msg[i] && i < 39) { buf[i] = msg[i]; i++; }
    while (i < 40) buf[i++] = ' ';
    buf[40] = '\0';
    VDP_setTextPalette(PAL0);
    VDP_drawText(buf, 0, STATUS_ROW);
    VDP_waitVSync();
}

/* -------------------------------------------------------------------------
 * draw_countdown() — row COUNTDOWN_ROW, col COUNTDOWN_COL, white (PAL0)
 * ---------------------------------------------------------------------- */
static void draw_countdown(u16 frame)
{
    int secs = (int)((UPDATE_FRAMES - frame) / FPS);
    char buf[42];
    if (last_update_str[0])
        sprintf(buf, "Next Update:%2ds %s  ", secs, last_update_str);
    else
        sprintf(buf, "Next Update: %2ds                    ", secs);
    VDP_setTextPalette(PAL0); /* white */
    VDP_drawText(buf, COUNTDOWN_COL, COUNTDOWN_ROW);
}

/* -------------------------------------------------------------------------
 * Minimal JSON number parser
 * ---------------------------------------------------------------------- */
static s32 parse_json_price_cents(const char *body, const char *key)
{
    const char *p = find_substr(body, key);
    if (!p) return 0x7FFFFFFF;
    p = find_char(p, ':');
    if (!p) return 0x7FFFFFFF;
    p++;
    while (*p == ' ') p++;
    int sign = 1;
    if (*p == '-') { sign = -1; p++; }
    long ipart = 0;
    while (*p >= '0' && *p <= '9') { ipart = ipart * 10 + (*p - '0'); p++; }
    long frac = 0; int fdig = 0;
    if (*p == '.') {
        p++;
        while (*p >= '0' && *p <= '9' && fdig < 2) { frac = frac * 10 + (*p - '0'); fdig++; p++; }
        while (*p >= '0' && *p <= '9') p++;
    }
    if (fdig == 1) frac *= 10;
    return sign * (s32)(ipart * 100 + frac);
}

/* -------------------------------------------------------------------------
 * fetch_quote_https()
 * Status line is silent on success; shows error in red on failure.
 * ---------------------------------------------------------------------- */
static void fetch_quote_https(u8 idx)
{
    uint8_t ch = MW_HTTP_CH;
    int16_t cr = 0, ue = 0, me = 0, oe = 0, re = 0;
    int16_t http_status = 0;
    int16_t recv_len = 0;
    uint32_t content_len = 0;
    char url[160];
    char dbg[42];
    s32 price = 0x7FFFFFFF, prev = 0x7FFFFFFF;
    const char *sym = tickers[idx].symbol;

    cr = mw_http_cleanup();

    sprintf(url, FINNHUB_URL_FMT, sym, FINNHUB_TOKEN);
    ue = mw_http_url_set(url);
    if (ue) goto done;

    me = mw_http_method_set(MW_HTTP_METHOD_GET);
    if (me) goto done;

    mw_http_header_add("Accept", "application/json");

    oe = mw_http_open(0);
    if (oe) goto done;

    http_status = mw_http_finish(&content_len, MS_TO_FRAMES(12000));
    if (http_status < 200 || http_status >= 300 || content_len == 0) {
        mw_http_cleanup();
        goto done;
    }

    recv_len = (content_len < (uint32_t)(MW_BUFLEN - 2))
             ? (int16_t)content_len : (int16_t)(MW_BUFLEN - 2);
    re = mw_recv_sync(&ch, (char*)cmd_buf, &recv_len, MS_TO_FRAMES(12000));
    /* Parse BEFORE cleanup — cleanup reuses cmd_buf */
    if (!re && recv_len > 0) {
        ((char*)cmd_buf)[recv_len] = '\0';
        price = parse_json_price_cents((char*)cmd_buf, "\"c\"");
        prev  = parse_json_price_cents((char*)cmd_buf, "\"pc\"");
    }
    mw_http_cleanup();

done:
    {
        bool ok = (ue == 0 && me == 0 && oe == 0 &&
                   http_status >= 200 && http_status < 300 &&
                   re == 0 && recv_len > 0 &&
                   price != 0x7FFFFFFF && prev != 0x7FFFFFFF);
        if (ok) {
            status_clear();
        } else {
            sprintf(dbg, "[%-5s] c=%d u=%d m=%d o=%d f=%d r=%d",
                    sym, (int)cr, (int)ue, (int)me, (int)oe,
                    (int)http_status, (int)re);
            status_err(dbg);
        }
    }

    if (ue || me || oe || re || recv_len <= 0 ||
        price == 0x7FFFFFFF || prev == 0x7FFFFFFF ||
        http_status < 200 || http_status >= 300)
        return;

    tickers[idx].price_cents      = price;
    tickers[idx].prev_close_cents = prev;
    tickers[idx].valid            = true;
    /* Stamp the CST time this quote arrived; also update last_update_str */
    {
        uint32_t dt[2] = {0, 0};
        char *ts = mw_date_time_get(dt);
        /* ts: "WWW MMM DD HH:MM:SS YYYY" (ctime format, 24 chars + NUL) */
        if (ts && ts[11] && ts[16]) {
            uint8_t hh = (uint8_t)((ts[11]-'0')*10 + (ts[12]-'0'));
            const char *ampm = (hh >= 12) ? "PM" : "AM";
            if (hh == 0) hh = 12;
            else if (hh > 12) hh -= 12;
            /* "HH:MM AM\0" = 9 bytes, fits in timestamp[12] */
            sprintf(tickers[idx].timestamp, "%2d:%c%c %s",
                    (int)hh, ts[14], ts[15], ampm);
            /* Full date+time for countdown row: "mm/dd/yy H:MM:SS XM" */
            {
                u8 mon = month_from_abbr(&ts[4]);
                u8 day = (u8)((ts[8] == ' ' ? 0 : (ts[8]-'0')*10) + (ts[9]-'0'));
                u8 yr  = (u8)((ts[22]-'0')*10 + (ts[23]-'0'));
                u8 mm2 = (u8)((ts[14]-'0')*10 + (ts[15]-'0'));
                u8 ss  = (u8)((ts[17]-'0')*10 + (ts[18]-'0'));
                sprintf(last_update_str, "%02d/%02d/%02d %d:%02d:%02d %s",
                        (int)mon, (int)day, (int)yr,
                        (int)hh, (int)mm2, (int)ss, ampm);
            }
        }
    }
}

static void fetch_quote(u8 idx)
{
    if (tickers[idx].symbol[0] == '\0') return;
    fetch_quote_https(idx);
}

/* -------------------------------------------------------------------------
 * rebuild_marquee()
 * ---------------------------------------------------------------------- */
static void rebuild_marquee(void)
{
    u8    write_idx = 1 - marquee_read_idx;
    char *p   = marquee_buf[write_idx];
    u8   *pp  = marquee_pal_buf[write_idx];
    u8 i, j, n, sym_len, white_len;

    for (i = 0; i < MAX_TICKERS; i++) {
        if (tickers[i].symbol[0] == '\0') continue;
        sym_len   = (u8)strlen(tickers[i].symbol);
        white_len = 1 + sym_len + 1; /* leading space + symbol + ':' */

        if (!tickers[i].valid) {
            n = (u8)sprintf(p, " %s:---.- ", tickers[i].symbol);
            for (j = 0; j < n; j++) pp[j] = PAL0;
            p += n; pp += n;
            continue;
        }
        {
            s32 price  = tickers[i].price_cents;
            s32 pc     = tickers[i].prev_close_cents;
            s32 delta  = price - pc;
            char sign  = (delta >= 0) ? '+' : '-';
            s32 adelta = (delta < 0) ? -delta : delta;
            u8  pal    = (delta >= 0) ? PAL1 : PAL2; /* green / red */

            n = (u8)sprintf(p, " %s:$%ld.%02ld(%c%ld.%02ld) ",
                         tickers[i].symbol,
                         (long)(price / 100),  (long)(price % 100),
                         sign,
                         (long)(adelta / 100), (long)(adelta % 100));
            /* " SYMBOL:" portion = white; price/delta portion = green/red */
            for (j = 0; j < white_len && j < n; j++) pp[j] = PAL0;
            for (      ; j < n;                 j++) pp[j] = pal;
            p += n; pp += n;
        }
    }
    *p  = '\0';
    *pp = PAL0;
    marquee_pending = true;
}

/* -------------------------------------------------------------------------
 * init_nasdaq_bg()  [compiled only when BCKGND_ON is defined]
 * Loads money-tile background into BG_B with diagonal scroll.
 * To enable: make BCKGND_ON=1  (or -DBCKGND_ON in CFLAGS)
 * ---------------------------------------------------------------------- */
#ifdef BCKGND_ON
/* Solid-black masking tile for BG_A text rows (all pixels = PAL0 index 1). */
#define BLACK_TILE_IDX  (TILE_FONT_INDEX - 1)
static const u32 black_tile_data[8] = {
    0x11111111, 0x11111111, 0x11111111, 0x11111111,
    0x11111111, 0x11111111, 0x11111111, 0x11111111
};

static void init_nasdaq_bg(void)
{
    u16 col, row;
    u16 i;
    PAL_setColors(PAL3 * 16, nasdaq_bg.palette->data, 15, CPU);
    PAL_setColor(63, RGB24_TO_VDPCOLOR(0x00AAFF));

    VDP_loadTileSet(nasdaq_bg.tileset, TILE_USER_INDEX, DMA);
    VDP_waitDMACompletion();

    /* Mask text rows + net worth row on BG_A; open rows show BG_B. */
    {
        u16 black = TILE_ATTR_FULL(PAL0, FALSE, FALSE, FALSE, BLACK_TILE_IDX);
        u16 r;
        VDP_loadTileData(black_tile_data, BLACK_TILE_IDX, 1, CPU);
        VDP_fillTileMapRect(BG_A, black, 0, 0, 40, PRICE_START_ROW);
        for (r = 0; r < MAX_TICKERS; r++)
            VDP_fillTileMapRect(BG_A, black, 0,
                                PRICE_START_ROW + r * PRICE_ROW_STRIDE, 40, 1);
        VDP_fillTileMapRect(BG_A, black, 0, NET_WORTH_ROW, 40, 1);
    }

    /* Tile BG_B (64×32) with the 16×8 pattern (64÷16=4, 32÷8=4 — seamless). */
    for (row = 0; row < 32; row++) {
        for (col = 0; col < 64; col++) {
            u16 src      = (u16)((row % 8) * 16 + (col % 16));
            u16 tile_idx = nasdaq_bg.tilemap->tilemap[src];
            u16 attr     = TILE_ATTR_FULL(PAL3, FALSE, FALSE, FALSE,
                                          TILE_USER_INDEX + tile_idx);
            VDP_setTileMapXY(BG_B, attr, col, row);
        }
    }

    for (i = 0; i < 32; i++) bgb_hscroll[i] = 0;
    VDP_setHorizontalScrollTile(BG_B, 0, bgb_hscroll, 32, DMA);
    { s16 zero = 0; VDP_setHorizontalScrollTile(BG_A, 0, &zero, 1, CPU); }
    VDP_setVerticalScroll(BG_B, 0);
}
#endif /* BCKGND_ON */

/* -------------------------------------------------------------------------
 * write_marquee_stream_tile() — write one character into the tilemap.
 * tile_col : column in the 64-wide BG_A plane (0..63)
 * str_pos  : index into the current marquee string (before % slen)
 * ---------------------------------------------------------------------- */
static void write_marquee_stream_tile(u16 tile_col, u16 str_pos)
{
    const char *buf    = marquee_buf[marquee_read_idx];
    const u8   *palbuf = marquee_pal_buf[marquee_read_idx];
    u16 slen = (u16)strlen(buf);
    u8  ch, pal;
    u16 tile_idx;

    if (!slen) return;
    ch       = (u8)buf[str_pos % slen];
    pal      = palbuf[str_pos % slen];
    tile_idx = (ch >= 0x20 && ch <= 0x7E)
             ? (TILE_FONT_INDEX + (ch - 0x20))
             : TILE_FONT_INDEX;
    VDP_setTileMapXY(BG_A,
        TILE_ATTR_FULL(pal, FALSE, FALSE, FALSE, tile_idx),
        tile_col, MARQUEE_ROW);
}

/* -------------------------------------------------------------------------
 * update_marquee_scroll() — must be first call after VDP_waitVSync so the
 * scroll register write lands in VBlank before active display begins.
 * Rewrites tilemap only when new content is pending (infrequent).
 * ---------------------------------------------------------------------- */
static void update_marquee_scroll(void)
{
    s16 neg_scroll;
#ifdef BCKGND_ON
    u16 i;
#endif

    if (marquee_pending) {
        u16 c;
        marquee_read_idx = 1 - marquee_read_idx;
        marquee_pending  = false;
        /* Initial fill: starting from the current scroll position, write 64
         * chars into the 64-tile circular tilemap so the visible window is
         * immediately populated with fresh content (chars 0..63). */
        for (c = 0; c < 64; c++)
            write_marquee_stream_tile((u16)((scroll_x / 8 + c) % 64), c);
        marquee_str_pos = 64; /* next char to stream after the initial fill */
    }

    /* --- Marquee: BG_A row 27 scrolls independently (HSCROLL_TILE) --- */
    scroll_x  = (scroll_x + TICKER_SCROLL_SPEED) & 0x1FF;
    neg_scroll = -(s16)scroll_x;
    VDP_setHorizontalScrollTile(BG_A, MARQUEE_ROW, &neg_scroll, 1, CPU);

    /* Stream the next char each time the scroll crosses a tile boundary. */
    if ((scroll_x % 8) == 0) {
        u16 exited = (u16)((scroll_x / 8 + 63) % 64);
        write_marquee_stream_tile(exited, marquee_str_pos);
        marquee_str_pos++;
    }

#ifdef BCKGND_ON
    /* --- NASDAQ background: BG_B diagonal scroll (+1X +1Y per frame) --- */
    bg_sx = (s16)((bg_sx + 1) & 0x1FF);
    bg_sy = (s16)((bg_sy + 1) & 0xFF);

    neg_scroll = -(s16)bg_sx;
    for (i = 0; i < 32; i++) bgb_hscroll[i] = neg_scroll;
    VDP_setHorizontalScrollTile(BG_B, 0, bgb_hscroll, 32, DMA);
    VDP_setVerticalScroll(BG_B, (s16)bg_sy);
#endif
}

/* -------------------------------------------------------------------------
 * fetch_draw_hook() — called by mw_command() once per frame while waiting
 * for an ESP32 HTTP response.  Keeps the display alive during the ~0.5–1.5 s
 * round-trip that would otherwise freeze the screen.
 * Also increments rng_seed_ctr to accumulate entropy from WiFi init timing.
 * ---------------------------------------------------------------------- */
static void fetch_draw_hook(void)
{
    static u16 hook_frame = 0;
    rng_seed_ctr++;
    /* TSK_superPend(1) already consumed a VBlank before this hook fires —
     * do NOT call VDP_waitVSync() here (re-entrant into SGDK task system).
     * Skip tile draws when the search popup is covering the screen. */
    update_marquee_scroll();
    if (!ticker_search_active())
        draw_price_row((u8)(hook_frame % MAX_TICKERS));
    hook_frame++;
}

/* -------------------------------------------------------------------------
 * format_net_worth() — format s64 cents as "$NNN,NNN,NNN,NNN" whole dollars
 * (no cents displayed).  Handles up to 999 billion dollars.
 * ---------------------------------------------------------------------- */
static void format_net_worth(char *buf, s64 total_cents)
{
    s64 dollars;
    u32 bil, rem, mil, tho, hun;

    if (total_cents < 0) total_cents = 0;
    dollars = total_cents / (s64)100;

    bil = (u32)(dollars / (s64)1000000000);
    rem = (u32)(dollars % (s64)1000000000);
    mil = rem / 1000000;
    tho = (rem / 1000) % 1000;
    hun = rem % 1000;

    if (bil > 0)
        sprintf(buf, "$%lu,%03lu,%03lu,%03lu",
                (unsigned long)bil, (unsigned long)mil,
                (unsigned long)tho, (unsigned long)hun);
    else if (mil > 0)
        sprintf(buf, "$%lu,%03lu,%03lu",
                (unsigned long)mil, (unsigned long)tho, (unsigned long)hun);
    else if (tho > 0)
        sprintf(buf, "$%lu,%03lu",
                (unsigned long)tho, (unsigned long)hun);
    else
        sprintf(buf, "$%lu", (unsigned long)hun);
}

/* -------------------------------------------------------------------------
 * draw_net_worth() — NET_WORTH_ROW, white label + cyan value
 * ---------------------------------------------------------------------- */
static void draw_net_worth(void)
{
    s64  total_cents = 0;
    char amtbuf[20];
    char line[42];
    u8   i, used;

    for (i = 0; i < MAX_TICKERS; i++) {
        if (tickers[i].valid && tickers[i].shares_owned > 0)
            total_cents += (s64)tickers[i].price_cents * (s64)tickers[i].shares_owned;
    }

    format_net_worth(amtbuf, total_cents);

    /* "Net Worth: " = 11 chars, then value in cyan, then spaces to end of line */
    VDP_setTextPalette(PAL0);
    VDP_drawText("Net Worth: ", 0, NET_WORTH_ROW);
    VDP_setTextPalette(PAL3);
    VDP_drawText(amtbuf, 11, NET_WORTH_ROW);
    used = (u8)(11 + strlen(amtbuf));
    for (i = 0; i < (u8)(40 - used) && i < sizeof(line) - 1; i++) line[i] = ' ';
    line[i] = '\0';
    VDP_setTextPalette(PAL0);
    if (i > 0) VDP_drawText(line, used, NET_WORTH_ROW);
}

/* -------------------------------------------------------------------------
 * draw_price_panel()
 * ---------------------------------------------------------------------- */
/* draw_price_panel()
 * Layout (40-char screen, all columns 0-based):
 *   col  0      cursor '>' if selected, ' ' otherwise
 *   col  1- 5  symbol (company color)
 *   col  6-15  price  "$%-4ld.%02ld  " = 10 chars fixed (white)
 *   col 16-29  delta  " %c%ld.%02ld(%c%ld.%02ld%%)" padded to 14 (green/red)
 *   col 30-37  time   "HH:MM xM"  8 chars (white) — or share count when selected
 *
 * No row-clear: each field is padded to a fixed width so it always
 * overwrites any stale characters from a previous shorter value.
 */
/* Draw a single ticker row — called once per frame (rolling) to avoid stutter. */
static void draw_price_row(u8 i)
{
    char pricebuf[16];
    char deltabuf[20];
    char rightbuf[12]; /* timestamp or share count */
    u8   n;
    u8   row = PRICE_START_ROW + i * PRICE_ROW_STRIDE;
    bool sel = (selected == (s8)i);

    /* skip empty slots — clear the row */
    if (tickers[i].symbol[0] == '\0') {
        VDP_setTextPalette(PAL0);
        VDP_drawText("                                        ", 0, row);
        return;
    }

    /* cursor */
    VDP_setTextPalette(sel ? PAL1 : PAL0);
    VDP_drawText(sel ? ">" : " ", 0, row);

    /* symbol: company logo color (GOOGL gets per-letter treatment) */
    if (is_googl(i)) {
        draw_googl_symbol(1, row);
    } else {
        VDP_setTextPalette(ticker_sym_pal[i]);
        VDP_drawText(tickers[i].symbol, 1, row);
        /* pad to 5 chars */
        {
            u8 slen = (u8)strlen(tickers[i].symbol);
            if (slen < 5) {
                char pad[5] = "     ";
                pad[5 - slen] = '\0';
                VDP_setTextPalette(PAL0);
                VDP_drawText(pad, (u16)(1 + slen), row);
            }
        }
    }

    if (!tickers[i].valid) {
        VDP_setTextPalette(PAL0);
        VDP_drawText("  Loading...              ", 6, row);
        return;
    }

    {
        s32 price  = tickers[i].price_cents;
        s32 pc     = tickers[i].prev_close_cents;
        s32 delta  = price - pc;
        char sign  = (delta >= 0) ? '+' : '-';
        s32 adelta = (delta < 0) ? -delta : delta;
        s32 pct    = pc ? (adelta * 10000L / pc) : 0;

        /* price — 10 chars fixed */
        n = (u8)sprintf(pricebuf, "$%ld.%02ld",
                        (long)(price / 100), (long)(price % 100));
        while (n < 10) pricebuf[n++] = ' ';
        pricebuf[10] = '\0';
        VDP_setTextPalette(PAL0);
        VDP_drawText(pricebuf, 6, row);

        /* delta — 14 chars fixed (green/red) */
        n = (u8)sprintf(deltabuf, " %c%ld.%02ld(%c%ld.%02ld%%)",
                        sign, (long)(adelta / 100), (long)(adelta % 100),
                        sign, (long)(pct / 100),    (long)(pct % 100));
        while (n < 14) deltabuf[n++] = ' ';
        deltabuf[14] = '\0';
        if (delta < 0)
            VDP_setTextPalette(PAL2); /* red   — loss */
        else
            VDP_setTextPalette(PAL1); /* green — gain */
        VDP_drawText(deltabuf, 16, row);

        /* right column (col 30-39) — share count if selected, timestamp otherwise */
        if (sel) {
            /* show share count; blink brackets when editing */
            u32 sh = tickers[i].shares_owned;
            char lbr = editing ? '[' : ' ';
            char rbr = editing ? ']' : ' ';
            if (sh >= 100000)
                sprintf(rightbuf, "%c%6lu%c   ", lbr, (unsigned long)sh, rbr);
            else if (sh >= 10000)
                sprintf(rightbuf, "%c%5lu%c    ", lbr, (unsigned long)sh, rbr);
            else
                sprintf(rightbuf, "%c%4lu%c     ", lbr, (unsigned long)sh, rbr);
            VDP_setTextPalette(PAL3); /* cyan for share count */
            VDP_drawText(rightbuf, 30, row);
        } else {
            VDP_setTextPalette(PAL0);
            VDP_drawText(tickers[i].timestamp, 30, row);
            VDP_drawText("  ", 38, row); /* clear any leftover */
        }
    }
}

static void draw_price_panel(void)
{
    u8 i;
    for (i = 0; i < MAX_TICKERS; i++)
        draw_price_row(i);
    draw_net_worth();
}

